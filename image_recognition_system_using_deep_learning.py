"""**13: Load the ResNet50 base model**

Following code load a ResNet50 model pre-trained on ImageNet. The input image size is (224, 224, 3), and we exclude the fully connected layer (classifier) used for ImageNet while using the weights learned on ImageNet. GlobalAveragePooling is used as the final pooling layer. The convolutional layers of the ImageNet-trained ResNet50 act as an eye to extract universal features (edges, textures, shapes, etc.) from the images. By "freezing" the weights of this part, we allow the learning to focus on the newly added classification layer and prevent the destruction of the knowledge acquired during pre-training.
"""

