"""**14: Setting up Callbacks to Support Training**

- **ModelCheckpoint**: Saves the model weights only when the validation accuracy (val_accuracy) improves. This ensures that the best-performing model during training is preserved.

- **EarlyStopping**: Stops training if the validation loss (val_loss) does not improve for 5 consecutive epochs.This helps prevent overfitting and saves computation time when model performance plateaus. With restore_best_weights=True, it automatically restores the weights from the epoch with the best validation performance.

- **ReduceLROnPlateau**: Reduces the learning rate to 20% of its current value if the validation loss (val_loss) does not improve for 3 epochs.
This allows the model to fine-tune its weights more precisely when learning stagnates.
"""

# This allbacks monitor the training process and automatically make adjustments

# --- ModelCheckpoint ---
checkpoint_path = "animal_classification_resnet50_checkpoint.weights.h5"
checkpoint_callback = ModelCheckpoint(
    filepath=checkpoint_path,
    save_weights_only=True, # Saves only the learned "weights"
    monitor="val_accuracy", # Use validation accuracy
    save_best_only=True, # Saves the model only for epochs that achieved a new best record
    verbose=1
)

# --- EarlyStopping ---
early_stopping = EarlyStopping(
    monitor="val_loss", # use validation loss
    patience=25, # Stops training if there is no improvement for 25 consecutive epochs
    restore_best_weights=True
)

# --- ReduceLROnPlateau ---
reduce_lr = ReduceLROnPlateau(
    monitor='val_loss', # use validation loss
    factor=0.2, # Multiplies the current learning rate by 0.2
    patience=3, # Lowers the learning rate if there is no improvement for 3 consecutive epochs
    min_lr=1e-6, # Lower bound for the learning rate
    verbose=1
)

