"""**9: Error Level Analysis**

The purpose of this code is to visualize the difference (error level) when a single image is saved with different JPEG compression quality settings, a technique known as Error Level Analysis (ELA). This is used to detect whether an image has been manipulated or edited.
"""

# ELA (Error Level Analysis) function
"""
The original image is loaded, re-compressed as a JPEG, and compared to the original image.
If the image hasn't been tampered with, the difference will be minimal because the entire image is uniformly compressed.
If only a portion has been altered, the error (or discrepancy) will appear significantly larger only in that area.
"""
def compute_ela_pil(path, quality):
    original_image = Image.open(path).convert('RGB')
    buffer = io.BytesIO()
    original_image.save(buffer, format='JPEG', quality=quality)
    buffer.seek(0)
    resaved_image = Image.open(buffer)
    ela_image = ImageChops.difference(original_image, resaved_image)
    extrema = ela_image.getextrema()
    max_diff = max([ex[1] for ex in extrema])

    if max_diff == 0:
        max_diff = 1

    scale = 255.0 / max_diff
    ela_image = ImageEnhance.Brightness(ela_image).enhance(scale)

    return np.array(ela_image)


